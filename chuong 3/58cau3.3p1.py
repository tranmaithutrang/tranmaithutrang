VAT=10/100
if x>=0:
    if 1<= x <= 100:
        y=x*550+VAT
    if 101<= x <=150:
        y=100*550+(x-100)*750+VAT*(100*550+(x-100)*750)
    if 151<= x <=200:
        y= 550*100 + 750*50 + 950*(x-150)+VAT*(550*100 + 750*50 + 950*(x-150))
    if 201<= x:
        y=550*100 + 750*50 + 950*50 + 1350*(x-200)+VAT*(550*100 + 750*50 + 950*50 + 1350*(x-200))
print("Phai tra=",y,sep="")
 